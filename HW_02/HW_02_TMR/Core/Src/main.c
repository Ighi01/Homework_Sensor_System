/* USER CODE BEGIN 0 */
void play_note(note curr_note){
	__HAL_TIM_SET_AUTORELOAD(&htim1,curr_note.tmr_period);
	__HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_2,(curr_note.tmr_period)/2 );
	//the function set the value of the counter register. So we need to
	//subtract from the max value (10000-1) the value of the duration*10
	__HAL_TIM_SET_COUNTER(&htim10,10000-1-(curr_note.duration*10));
	HAL_TIM_Base_Start_IT(&htim10);
	HAL_TIM_PWM_Start(&htim1,TIM_CHANNEL_2);
	done = 0;
}

void play_song(note* LondonBridge){
	while(i<24){
		//only if flag done is 1 (timer has elapsed) and if start=1 then call the play_note function
		if(start && done){
			play_note(LondonBridge[i]);
			i++;
		}
	}
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	if(GPIO_Pin == GPIO_PIN_8){
		start = 1;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if(htim == &htim10){
		//only when the timer has elapsed, stop the pwm and set the flag done to 1
		HAL_TIM_PWM_Stop(&htim1,TIM_CHANNEL_2);
		done = 1;
	}
}
